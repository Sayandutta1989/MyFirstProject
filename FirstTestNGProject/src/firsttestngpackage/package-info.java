/*Advantages of TestNG over JUnit
There are three major advantages of TestNG over JUnit:

1. Annotations are easier to understand
2. Test cases can be grouped more easily
3. Parallel testing is possible
4. Annotations in TestNG are lines of code that can control how the method below them will be executed. 
They are always preceded by the @ symbol.


Why do we need TestNG in Selenium?
1. TestNG can generate reports based on our Selenium test results.
2. WebDriver has no native mechanism for generating reports.
3. TestNG can generate the report in a readable format
4. TestNG simplifies the way the tests are coded
5. There is no more need for a static main method in our tests. 
The sequence of actions is regulated by easy-to-understand annotations that do not require methods to be static.
6. Uncaught exceptions are automatically handled by TestNG without terminating the test prematurely.
These exceptions are reported as failed steps in the report. */